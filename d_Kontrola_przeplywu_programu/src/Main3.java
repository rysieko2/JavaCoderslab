public class Main3 {

	public static void main(String[] args) {
	/*	1. W pliku `Main3.java` stwórz pêtlê, która 10 razy wypisze w konsoli tekst: *"Lubiê Javê"*.
		2. Napisz zarówno pêtlê `for` jak i pêtlê `while`.
		3. W sumie napis bêdzie widoczny 20 razy.
	*/
		for (int x=0; x<10 ;x++ ){
			System.out.println("*\"Lubiê Javê\"*");
		}
		int x = 0;
		while (x<10){
			System.out.println("*\"Lubiê Javê\"*");
			x++;
		}
	}
}

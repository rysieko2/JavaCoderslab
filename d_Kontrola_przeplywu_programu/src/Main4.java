
public class Main4 {

	public static void main(String[] args) {
	/*	1. W pliku `Main4.java` stwórz pêtlê, która wypisze w konsoli liczby od 1 do 100, jedna w jednej linii.
		2. Napisz zarówno pêtlê `for` jak i pêtlê `while`.
		3. W sumie zostanie wypisanych 200 liczb.
	*/
		for (int x=1; x<=100 ;x++ ){
			System.out.println(x);
		}
		int x = 1;
		while (x<=100){
			System.out.println(x);
			x++;
		}
	}
}
